What are the benefits of using Object Oriented Programming in Ruby?
Think of as many as you can.

The benefits of using OOP in Ruby are that the code becomes modularized
which is easier to understand and maintain and reduces dependencies. Code can
be used in larger more complex projects and still logically and comprehensibly.
The DRY(Don't Repeat Yourself) pricipal is applied through reusing modules
and methods in many parts of the program.  This makes the code more efficient
when it is reusable. Security is improved by allowing only access to specific
classes or modules and being able to restrict the interface in encapsulated
objects. Code is able to be thought about abstractly.
To summarize: increase maintainability, security, DRY code, readability,
reusability, abstractable, ease of conceptualizing.

Provided solution:
Because there are so many benefits to using OOP we will just summarize some of
the major ones:

- Creating objects allows programmers to think more abstractly about the code
they are writing.

- Objects are represented by nouns so are easier to conceptualize.

- It allows us to only expose functionality to the parts of code that need it,
meaning namespace issues are much harder to come across.

- It allows us to easily give functionality to different parts of an application
without duplication.

- We can build applications faster as we can reuse pre-written code.

- As the software becomes more complex this complexity can be more easily
managed.

