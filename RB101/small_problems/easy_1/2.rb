#  input: 1 integer (pos, 0, neg) valid integer value
#  output: return true if numbers absolute value is odd 
#  integer value is valid
#  E - test cases given
#  D - integers
#  A - If integer % 2 remainder is not 0 it is odd and return boolean true 

def is_odd?(number)
  number.abs % 2 != 0
end

puts is_odd?(2)    # => false
puts is_odd?(5)    # => true
puts is_odd?(-17)  # => true
puts is_odd?(-8)   # => false
puts is_odd?(0)    # => false
puts is_odd?(7)    # => true

